'use client';

import { useEffect, useState } from 'react';
import { useRouter } from 'next/navigation';
import { Header } from '@/components/Header';
import { MetricsPanel } from '@/components/MetricsPanel';
import { ResultsChart } from '@/components/ResultsChart';
import { Button } from '@/components/ui/button';
import { Card, CardContent, CardDescription, CardHeader, CardTitle } from '@/components/ui/card';
import { DetectionResult } from '@/types';
import { ArrowLeft, Download } from 'lucide-react';

export default function ResultsPage() {
  const router = useRouter();
  const [result, setResult] = useState<DetectionResult | null>(null);
  const [loading, setLoading] = useState(true);

  useEffect(() => {
    // Retrieve result from sessionStorage
    const storedResult = sessionStorage.getItem('detectionResult');
    if (storedResult) {
      try {
        const parsed = JSON.parse(storedResult);
        setResult(parsed);
      } catch (error) {
        console.error('[v0] Failed to parse result:', error);
        router.push('/');
      }
    } else {
      router.push('/');
    }
    setLoading(false);
  }, [router]);

  const downloadReport = () => {
    if (!result) return;

    const report = `
ExoDetect AI - Analysis Report
================================

DETECTION RESULT
----------------
Status: ${result.isExoplanet ? 'EXOPLANET DETECTED' : 'NO EXOPLANET DETECTED'}
Confidence: ${result.confidence.toFixed(2)}%
Message: ${result.message}

KEY METRICS
-----------
Orbital Period: ${result.orbitalPeriod ? result.orbitalPeriod.toFixed(4) : 'N/A'} days
Transit Depth: ${result.transitDepth ? result.transitDepth.toFixed(4) : 'N/A'} ppm
Signal-to-Noise Ratio: ${result.snr ? result.snr.toFixed(4) : 'N/A'}
Dominant Frequency: ${result.dominantFrequency ? result.dominantFrequency.toFixed(6) : 'N/A'} cycles/day

DATA SUMMARY
------------
Total Data Points (Original): ${result.processedData.original.time.length}
Time Range: ${Math.min(...result.processedData.original.time).toFixed(2)} to ${Math.max(...result.processedData.original.time).toFixed(2)} JD
Flux Range (Original): ${Math.min(...result.processedData.original.flux).toFixed(6)} to ${Math.max(...result.processedData.original.flux).toFixed(6)}

METHODOLOGY
-----------
The analysis uses the following signal processing techniques:
1. Data Normalization: Scaling flux values to 0-1 range
2. Noise Reduction: Median filtering with 5-point window
3. Detrending: Polynomial (degree 2) fit subtraction
4. Frequency Analysis: Fast Fourier Transform (FFT)
5. Peak Detection: Identification of dominant periodic signals
6. Transit Detection: Depth and SNR calculation at detected periods

The confidence score is computed from:
- Transit Depth Contribution (40% max)
- Signal-to-Noise Ratio (35% max)
- Peak Power Normalization (25% max)

A minimum confidence threshold of 40% is required for exoplanet detection.

Generated by ExoDetect AI
Analysis Date: ${new Date().toISOString()}
    `.trim();

    const element = document.createElement('a');
    element.setAttribute(
      'href',
      'data:text/plain;charset=utf-8,' + encodeURIComponent(report)
    );
    element.setAttribute('download', 'exoplanet_analysis_report.txt');
    element.style.display = 'none';
    document.body.appendChild(element);
    element.click();
    document.body.removeChild(element);
  };

  if (loading) {
    return (
      <>
        <Header />
        <main className="min-h-screen bg-gradient-to-b from-background via-background to-muted/20 flex items-center justify-center">
          <div className="text-center">
            <div className="h-12 w-12 border-4 border-primary border-t-transparent rounded-full animate-spin mx-auto mb-4"></div>
            <p className="text-muted-foreground">Loading analysis results...</p>
          </div>
        </main>
      </>
    );
  }

  if (!result) {
    return (
      <>
        <Header />
        <main className="min-h-screen bg-gradient-to-b from-background via-background to-muted/20">
          <div className="container mx-auto px-4 py-12">
            <div className="max-w-2xl mx-auto text-center">
              <p className="text-muted-foreground mb-4">No analysis results found.</p>
              <Button onClick={() => router.push('/')}>
                <ArrowLeft className="h-4 w-4 mr-2" />
                Return to Upload
              </Button>
            </div>
          </div>
        </main>
      </>
    );
  }

  return (
    <>
      <Header />
      <main className="min-h-screen bg-gradient-to-b from-background via-background to-muted/20">
        <div className="container mx-auto px-4 py-12">
          {/* Header with Back Button */}
          <div className="mb-8 flex items-center justify-between">
            <div>
              <h2 className="text-3xl font-bold mb-2">Analysis Complete</h2>
              <p className="text-muted-foreground">
                {result.isExoplanet
                  ? 'An exoplanet candidate has been detected in your data'
                  : 'No exoplanet signature was detected in your data'}
              </p>
            </div>
            <div className="flex gap-3">
              <Button
                variant="outline"
                onClick={() => router.push('/')}
              >
                <ArrowLeft className="h-4 w-4 mr-2" />
                New Analysis
              </Button>
              <Button onClick={downloadReport}>
                <Download className="h-4 w-4 mr-2" />
                Download Report
              </Button>
            </div>
          </div>

          {/* Metrics Panel */}
          <MetricsPanel
            isExoplanet={result.isExoplanet}
            confidence={result.confidence}
            orbitalPeriod={result.orbitalPeriod}
            transitDepth={result.transitDepth}
            snr={result.snr}
            message={result.message}
          />

          {/* Analysis Message */}
          <Card className="mb-8">
            <CardHeader>
              <CardTitle>Analysis Summary</CardTitle>
            </CardHeader>
            <CardContent>
              <p className="text-lg text-foreground">{result.message}</p>
            </CardContent>
          </Card>

          {/* Visualizations */}
          <div className="mb-8">
            <ResultsChart result={result} />
          </div>

          {/* Technical Details */}
          <Card>
            <CardHeader>
              <CardTitle>Technical Details</CardTitle>
              <CardDescription>
                Information about the analysis process and data characteristics
              </CardDescription>
            </CardHeader>
            <CardContent>
              <div className="grid md:grid-cols-2 gap-8">
                <div>
                  <h4 className="font-semibold mb-4">Data Characteristics</h4>
                  <dl className="space-y-2 text-sm">
                    <div className="flex justify-between">
                      <dt className="text-muted-foreground">Data Points:</dt>
                      <dd className="font-mono">{result.processedData.original.time.length}</dd>
                    </div>
                    <div className="flex justify-between">
                      <dt className="text-muted-foreground">Time Span:</dt>
                      <dd className="font-mono">
                        {(
                          Math.max(...result.processedData.original.time) -
                          Math.min(...result.processedData.original.time)
                        ).toFixed(2)}{' '}
                        days
                      </dd>
                    </div>
                    <div className="flex justify-between">
                      <dt className="text-muted-foreground">Flux Range (Original):</dt>
                      <dd className="font-mono">
                        {Math.min(...result.processedData.original.flux).toFixed(4)} to{' '}
                        {Math.max(...result.processedData.original.flux).toFixed(4)}
                      </dd>
                    </div>
                    <div className="flex justify-between">
                      <dt className="text-muted-foreground">Flux Range (Detrended):</dt>
                      <dd className="font-mono">
                        {Math.min(...result.processedData.detrended.flux).toFixed(4)} to{' '}
                        {Math.max(...result.processedData.detrended.flux).toFixed(4)}
                      </dd>
                    </div>
                  </dl>
                </div>
                <div>
                  <h4 className="font-semibold mb-4">Detection Thresholds</h4>
                  <dl className="space-y-2 text-sm">
                    <div className="flex justify-between">
                      <dt className="text-muted-foreground">Min Transit Depth:</dt>
                      <dd className="font-mono">50 ppm</dd>
                    </div>
                    <div className="flex justify-between">
                      <dt className="text-muted-foreground">Min SNR:</dt>
                      <dd className="font-mono">7.0</dd>
                    </div>
                    <div className="flex justify-between">
                      <dt className="text-muted-foreground">Min Peak Power:</dt>
                      <dd className="font-mono">0.01</dd>
                    </div>
                    <div className="flex justify-between">
                      <dt className="text-muted-foreground">Confidence Threshold:</dt>
                      <dd className="font-mono">40%</dd>
                    </div>
                  </dl>
                </div>
              </div>
            </CardContent>
          </Card>
        </div>
      </main>
    </>
  );
}
